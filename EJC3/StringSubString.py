#Metodos string index substring
text ="ABCDEFGHIJKLNMOPQRSTUVWXYZ"

result = text[2]
print(result)

result = text[5]
print(result)

result = text[2:5]
print(result)

result = text[:5]
print(result)

result = text[2:]
print(result)

result = text[2:10:2]
print(result)

result = text[::2]
print(result)

result = text[::-1]
print(result)